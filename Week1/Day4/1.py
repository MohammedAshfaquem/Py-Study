# OOP stands for Object-Oriented Programming, a programming paradigm based on the concept of "objects", which can contain data (fields or attributes) and code (methods or functions).
# Here are the four core OOP concepts:

# 1. Encapsulation
# Definition: Hiding the internal state and requiring all interaction to be performed through an object's methods.
# Goal: Protect the data and ensure it's used properly.
# Example: You access or modify object properties via getter/setter methods rather than directly.

# 2. Abstraction
# Definition: Hiding complex implementation details and showing only the necessary features.
# Goal: Reduce complexity and improve code readability.
# Example: A Car class has a drive() method, but you don't need to know how the engine works to use it.

# 3. Inheritance
# Definition: One class can inherit fields and methods from another class.
# Goal: Promote code reuse and create a hierarchy.
# Example: A Dog class inherits from an Animal class and automatically has attributes like eat() and sleep().

# 4. Polymorphism
# Definition: The ability for different classes to be treated as instances of the same class through a common interface.
# Goal: Write more flexible and reusable code.
# Example: A speak() method behaves differently in Dog and Cat classes, even though both are called