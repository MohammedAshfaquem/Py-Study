# ✅ What is PostgreSQL?
# PostgreSQL (pronounced "Post-gress-Q-L")
# it is a powerful, open-source object-relational database management system (ORDBMS).
# It is known for its reliability, flexibility,feature robustness .

# 🔑 Key Features of PostgreSQL:
# Feature               Description
# 💾 Open Source	      Free to use and modify under the PostgreSQL License.
# 🧠 Advanced SQL sprt  Supports standard SQL and powerful features like window functions, CTEs.
# 🔐 Security	          Role-based access control, SSL, authentication methods.
# 🔄 ACID               Compliance	Ensures reliable transactions with Atomicity, Consistency, Isolation, Durability.
# 🔁 MVCC               Multi-Version Concurrency Control allows high-performance transactions.
# 🌐 Cross-Platform	  Runs on Linux, Windows, and macOS.
# 🔌 Support            it Works with programming languages like Python, Java, Node.js, PHP, etc.


