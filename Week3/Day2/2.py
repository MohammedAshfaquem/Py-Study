# ✅ 1. Comparison Operators
# | Operator     | Description              | Example            |
# | ------------ | ------------------------ | ------------------ |
# | `=`          | Equal to                 | `age = 25`         |
# | `!=` or `<>` | Not equal to             | `name != 'Akshay'` |
# | `>`          | Greater than             | `age > 30`         |
# | `<`          | Less than                | `age < 20`         |
# | `>=`         | Greater than or equal to | `age >= 18`        |
# | `<=`         | Less than or equal to    | `age <= 60`        |

# ✅ 2. Logical Operators
# | Operator | Description                    | Example                                        |
# | -------- | ------------------------------ | ---------------------------------------------- |
# | `AND`    | All conditions must be true    | `age > 18 AND city = 'Delhi'`                  |
# | `OR`     | At least one condition is true | `age < 18 OR age > 60`                         |
# | `NOT`    | Reverses the condition         | `NOT (age = 25)` → matches rows where age ≠ 25 |


# ✅ 3. Arithmetic Operators
# | Operator | Description         | Example            |
# | -------- | ------------------- | ------------------ |
# | `+`      | Addition            | `salary + bonus`   |
# | `-`      | Subtraction         | `price - discount` |
# | `*`      | Multiplication      | `quantity * price` |
# | `/`      | Division            | `total / count`    |
# | `%`      | Modulus (remainder) | `score % 2`        |


# ✅ 4. Other Common Operators
# | Operator      | Description                           | Example                                  |
# | ------------- | ------------------------------------- | ---------------------------------------- |
# | `IN`          | Matches any value in a list           | `city IN ('Delhi', 'Mumbai')`            |
# | `BETWEEN`     | Range check (inclusive)               | `age BETWEEN 18 AND 30`                  |
# | `LIKE`        | Pattern match (with `%` or `_`)       | `name LIKE 'A%'` → names starting with A |
# | `IS NULL`     | Checks if value is null               | `email IS NULL`                          |
# | `IS NOT NULL` | Checks if value is not null           | `email IS NOT NULL`                      |
# | `EXISTS`      | Checks if a subquery returns any rows | `EXISTS (SELECT 1 FROM orders...)`       |



# EXTRA 
# | Pattern   | Matches              |
# | --------- | -------------------- |
# | `'A%'`    | Starts with A        |
# | `'%a'`    | Ends with a          |
# | `'%ksh%'` | Contains "ksh"       |
# | `'_____'` | Exactly 5 characters |

