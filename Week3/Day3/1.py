# Aggregate functions perform a calculation on multiple rows and return a single value.
# They Are used With Group By.

# | Function  | What it does             | Example Use               |
# | --------- | ------------------------ | ------------------------- |
# | `COUNT()` | Counts number of rows    | How many users are there? |
# | `SUM()`   | Adds values together     | Total salary paid?        |
# | `AVG()`   | Calculates average value | Average user age?         |
# | `MIN()`   | Finds the smallest value | Youngest user?            |
# | `MAX()`   | Finds the largest value  | Oldest user?              |
